package com.study.boot1.bean;

public class HouseVO {
	private  String id;
	private  String num;//成交数
	private  String rname;//小区名
	private  String lal;//定位

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getRname() {
		return rname;
	}
	public void setRname(String rname) {
		this.rname = rname;
	}
	public String getLal() {
		return lal;
	}
	public void setLal(String lal) {
		this.lal = lal;
	}



}
